    public void populaTabela() {
        File file = new File("login.txt");

        try {
            FileReader ler = new FileReader(file);
            BufferedReader lerb = new BufferedReader(ler);
            String linha = lerb.readLine();
            String dados[] = null;
            DefaultTableModel tabela = (DefaultTableModel) tUzer.getModel();
            tabela.setRowCount(0);
            
            for (int i = 0;; i++) {
                if(linha == null)
                    break;
                dados = linha.split(";");
                tabela.addRow(new Object[]{dados[2], dados[1]});
                
                linha = lerb.readLine();
            }
            ler.close();
            lerb.close();
        } catch (Exception e) {
                System.out.println("erro");
        }
    }

    

